import pytest
from app.main import get_human_age


@pytest.mark.parametrize("cat_age, dog_age, expected", [
    (0, 0, [0, 0]),
    (14, 14, [0, 0]),
    (15, 15, [1, 1]),
    (23, 23, [1, 1]),
    (24, 24, [2, 2]),
    (27, 27, [2, 2]),
    (28, 28, [3, 2]),
    (100, 100, [21, 17]),
    (16, 16, [1, 1]),
    (20, 20, [1, 1]),
    (24, 24, [2, 2]),
    (28, 28, [3, 2]),
    (32, 32, [4, 3]),
    (36, 36, [5, 4]),
    (40, 40, [6, 5]),
    (44, 44, [7, 6]),
    (48, 48, [8, 6]),
    (52, 52, [9, 7]),
    (56, 56, [10, 8]),
    (60, 60, [11, 9]),
    (64, 64, [12, 10]),
    (68, 68, [13, 10]),
    (72, 72, [14, 11]),
    (76, 76, [15, 12]),
    (80, 80, [16, 13]),
    (84, 84, [17, 14]),
    (88, 88, [18, 14]),
    (92, 92, [19, 15]),
    (96, 96, [20, 16]),
    (100, 100, [21, 17])
])
def test_get_human_age(
    cat_age: int,
    dog_age: int,
    expected: list[int]
) -> None:
    assert get_human_age(cat_age, dog_age) == expected
